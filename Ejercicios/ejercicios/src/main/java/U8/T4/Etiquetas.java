package U8.T4;

public class Etiquetas {
    static String nombre;
    static int edad;

    public Etiquetas() {
        this.nombre=nombre;
        this.edad=edad;
    }

    public static String getNombre() {
        return nombre;
    }

    public static void setNombre(String nombre) {
        Etiquetas.nombre = nombre;
    }

    public static int getEdad() {
        return edad;
    }

    public static void setEdad(int edad) {
        Etiquetas.edad = edad;
    }


}
